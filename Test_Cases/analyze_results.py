#!/usr/bin/env python3
"""
analyze_results.py - Parse Arcanum interaction experiment results

This script analyzes the structured logs generated by Custom_Test.py runs
with/without interaction scripts to quantify the impact of interactions on
detected security threats.

Usage:
    # Set environment before running tests:
    export ARCANUM_RESULTS_DIR="/root/arcanum_results"
    export ARCANUM_RESULTS_MODE="passive"
    python3.8 ~/Test_Cases/Custom_Test.py
    
    export ARCANUM_RESULTS_MODE="interactive"
    python3.8 ~/Test_Cases/Custom_Test.py
    
    # Then analyze:
    python3.8 ~/Test_Cases/analyze_results.py /root/arcanum_results

Output CSV format:
    target_page,extension,mode,timestamp,interaction_used,sources_count,storage_sinks,xhr_sinks,fetch_sinks
"""

import argparse
import csv
import glob
import json
import os
import sys
from collections import defaultdict


def count_taint_sources(v8logs_dir):
    """Count unique taint source entries in taint_sources.log"""
    sources_log = os.path.join(v8logs_dir, 'taint_sources.log')
    if not os.path.exists(sources_log):
        return 0
    
    with open(sources_log, 'r', encoding='utf-8', errors='ignore') as f:
        content = f.read()
        # Count ">>> Taint source:" markers
        return content.count('>>> Taint source:')


def count_sink_entries(user_data_dir, sink_type):
    """Count entries in a specific taint sink log"""
    sink_log = os.path.join(user_data_dir, f'taint_{sink_type}.log')
    if not os.path.exists(sink_log):
        return 0
    
    with open(sink_log, 'r', encoding='utf-8', errors='ignore') as f:
        lines = f.readlines()
        # Basic heuristic: non-empty lines indicate sink events
        return len([line for line in lines if line.strip()])


def parse_run_directory(run_dir):
    """Extract metrics from a single run directory"""
    metadata_path = os.path.join(run_dir, 'metadata.json')
    if not os.path.exists(metadata_path):
        return None
    
    with open(metadata_path, 'r') as f:
        metadata = json.load(f)
    
    v8logs_dir = os.path.join(run_dir, 'v8logs')
    user_data_dir = os.path.join(run_dir, 'user_data')
    
    return {
        'target_page': metadata.get('target_page', 'unknown'),
        'extension': metadata.get('extension', 'unknown'),
        'mode': metadata.get('mode', 'unknown'),
        'timestamp': metadata.get('timestamp', 'unknown'),
        'interaction_used': metadata.get('interaction_used', False),
        'sources_count': count_taint_sources(v8logs_dir),
        'storage_sinks': count_sink_entries(user_data_dir, 'storage'),
        'xhr_sinks': count_sink_entries(user_data_dir, 'xhr'),
        'fetch_sinks': count_sink_entries(user_data_dir, 'fetch')
    }


def find_all_runs(results_root):
    """Recursively find all run directories with metadata.json"""
    metadata_files = glob.glob(os.path.join(results_root, '**', 'metadata.json'), recursive=True)
    return [os.path.dirname(mf) for mf in metadata_files]


def generate_summary(parsed_runs):
    """Generate aggregate statistics comparing passive vs interactive modes"""
    by_mode = defaultdict(lambda: {'sources': 0, 'storage': 0, 'xhr': 0, 'fetch': 0, 'count': 0})
    
    for run in parsed_runs:
        mode = run['mode']
        by_mode[mode]['sources'] += run['sources_count']
        by_mode[mode]['storage'] += run['storage_sinks']
        by_mode[mode]['xhr'] += run['xhr_sinks']
        by_mode[mode]['fetch'] += run['fetch_sinks']
        by_mode[mode]['count'] += 1
    
    print("\n=== Summary by Mode ===")
    for mode, stats in sorted(by_mode.items()):
        print(f"\n{mode.upper()}:")
        print(f"  Runs: {stats['count']}")
        print(f"  Total taint sources: {stats['sources']}")
        print(f"  Total storage sinks: {stats['storage']}")
        print(f"  Total XHR sinks: {stats['xhr']}")
        print(f"  Total fetch sinks: {stats['fetch']}")
        if stats['count'] > 0:
            print(f"  Avg sources/run: {stats['sources'] / stats['count']:.1f}")
            print(f"  Avg sinks/run: {(stats['storage'] + stats['xhr'] + stats['fetch']) / stats['count']:.1f}")


def main():
    parser = argparse.ArgumentParser(description='Analyze Arcanum interaction experiment results')
    parser.add_argument('results_dir', help='Root directory containing experiment results')
    parser.add_argument('--csv', help='Output CSV file path', default='arcanum_analysis.csv')
    args = parser.parse_args()
    
    if not os.path.exists(args.results_dir):
        print(f"Error: Results directory not found: {args.results_dir}", file=sys.stderr)
        sys.exit(1)
    
    run_dirs = find_all_runs(args.results_dir)
    if not run_dirs:
        print(f"No run directories found in {args.results_dir}", file=sys.stderr)
        sys.exit(1)
    
    print(f"Found {len(run_dirs)} run directories")
    
    parsed_runs = []
    for run_dir in run_dirs:
        run_data = parse_run_directory(run_dir)
        if run_data:
            parsed_runs.append(run_data)
    
    if not parsed_runs:
        print("No valid run data found", file=sys.stderr)
        sys.exit(1)
    
    # Write CSV
    fieldnames = ['target_page', 'extension', 'mode', 'timestamp', 'interaction_used',
                  'sources_count', 'storage_sinks', 'xhr_sinks', 'fetch_sinks']
    
    with open(args.csv, 'w', newline='') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        for run in sorted(parsed_runs, key=lambda r: (r['target_page'], r['extension'], r['mode'], r['timestamp'])):
            writer.writerow(run)
    
    print(f"\nWrote {len(parsed_runs)} run records to {args.csv}")
    
    generate_summary(parsed_runs)
    
    print("\n=== Next Steps ===")
    print("1. Import the CSV into your analysis tool (Python/R/Excel)")
    print("2. Compare passive vs interactive mode for the same extension/page pairs")
    print("3. Calculate delta in sources/sinks to quantify interaction impact")
    print("4. Use matplotlib/seaborn to visualize distributions and improvements")


if __name__ == '__main__':
    main()

